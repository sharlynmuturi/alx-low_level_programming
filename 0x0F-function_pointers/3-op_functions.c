#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - add function
 * @a: int input
 * @b: int input
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction function
 * @a: int input
 * @b: int input
 * Return: subtract
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication function
 * @a: int input
 * @b: int input
 * Return: multiply
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: int input
 * @b: int input
 * Return: divide
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus function
 * @a: int input
 * @b: int input
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
